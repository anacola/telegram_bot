package br.com.eleicoes.model;

/**
 * Classe ConcentracaoDespesa.
 */
public class ConcentracaoDespesa {
	
	/** ds DRD. */
	private String dsDRD;
	
	/** qtde despesas. */
	private String qtdeDespesas;
	
	/** valor. */
	private double valor;
	
	/**
	 * Gets ds DRD.
	 *
	 * @return ds DRD
	 */
	public String getDsDRD() {
		return dsDRD;
	}
	
	/**
	 * Sets ds DRD.
	 *
	 * @param dsDRD new ds DRD
	 */
	public void setDsDRD(String dsDRD) {
		this.dsDRD = dsDRD;
	}
	
	/**
	 * Gets qtde despesas.
	 *
	 * @return qtde despesas
	 */
	public String getQtdeDespesas() {
		return qtdeDespesas;
	}
	
	/**
	 * Sets qtde despesas.
	 *
	 * @param qtdeDespesas new qtde despesas
	 */
	public void setQtdeDespesas(String qtdeDespesas) {
		this.qtdeDespesas = qtdeDespesas;
	}
	
	/**
	 * Gets valor.
	 *
	 * @return valor
	 */
	public double getValor() {
		return valor;
	}
	
	/**
	 * Sets valor.
	 *
	 * @param valor new valor
	 */
	public void setValor(double valor) {
		this.valor = valor;
	}
}
