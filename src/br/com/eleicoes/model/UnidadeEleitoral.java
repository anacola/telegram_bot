package br.com.eleicoes.model;

/**
 * Classe UnidadeEleitoral.
 */
public class UnidadeEleitoral {
	
	/** id. */
	private Object id;
	
	/** sigla. */
	private String sigla;
	
	/** nome. */
	private String nome;
	
	/** regiao. */
	private Object regiao;
	
	/** cargos. */
	private Object cargos;
	
	/** diretorios. */
	private Object diretorios;
	
	/** codigo. */
	private String codigo;
	
	/** capital. */
	private boolean capital;
	
	/** estado. */
	private Object estado;
	
	/**
	 * Gets id.
	 *
	 * @return id
	 */
	public Object getId() {
		return id;
	}
	
	/**
	 * Sets id.
	 *
	 * @param id new id
	 */
	public void setId(Object id) {
		this.id = id;
	}
	
	/**
	 * Gets sigla.
	 *
	 * @return sigla
	 */
	public String getSigla() {
		return sigla;
	}
	
	/**
	 * Sets sigla.
	 *
	 * @param sigla new sigla
	 */
	public void setSigla(String sigla) {
		this.sigla = sigla;
	}
	
	/**
	 * Gets nome.
	 *
	 * @return nome
	 */
	public String getNome() {
		return nome;
	}
	
	/**
	 * Sets nome.
	 *
	 * @param nome new nome
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	/**
	 * Gets regiao.
	 *
	 * @return regiao
	 */
	public Object getRegiao() {
		return regiao;
	}
	
	/**
	 * Sets regiao.
	 *
	 * @param regiao new regiao
	 */
	public void setRegiao(Object regiao) {
		this.regiao = regiao;
	}
	
	/**
	 * Gets cargos.
	 *
	 * @return cargos
	 */
	public Object getCargos() {
		return cargos;
	}
	
	/**
	 * Sets cargos.
	 *
	 * @param cargos new cargos
	 */
	public void setCargos(Object cargos) {
		this.cargos = cargos;
	}
	
	/**
	 * Gets diretorios.
	 *
	 * @return diretorios
	 */
	public Object getDiretorios() {
		return diretorios;
	}
	
	/**
	 * Sets diretorios.
	 *
	 * @param diretorios new diretorios
	 */
	public void setDiretorios(Object diretorios) {
		this.diretorios = diretorios;
	}
	
	/**
	 * Gets codigo.
	 *
	 * @return codigo
	 */
	public String getCodigo() {
		return codigo;
	}
	
	/**
	 * Sets codigo.
	 *
	 * @param codigo new codigo
	 */
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	
	/**
	 * Checks if is capital.
	 *
	 * @return true, if is capital
	 */
	public boolean isCapital() {
		return capital;
	}
	
	/**
	 * Sets capital.
	 *
	 * @param capital new capital
	 */
	public void setCapital(boolean capital) {
		this.capital = capital;
	}
	
	/**
	 * Gets estado.
	 *
	 * @return estado
	 */
	public Object getEstado() {
		return estado;
	}
	
	/**
	 * Sets estado.
	 *
	 * @param estado new estado
	 */
	public void setEstado(Object estado) {
		this.estado = estado;
	}
}
